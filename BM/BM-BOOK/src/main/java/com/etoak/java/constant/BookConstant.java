package com.etoak.java.constant;

/**
 * 书籍的常量
 * final 修饰 class 方法 属性
 * final 类
 *  不能被继承 没有子类 骡子
 * final 方法
 *  能被继承 不能被重写/覆盖
 * final 属性
 *  定义之后无法修改
 */
public class BookConstant {

    public static final Integer BOOK_STATUS_ZK = 0;
    public static final Integer BOOK_STATUS_YJC = 1;
    public static final Integer BOOK_STATUS_YDS = 2;
}
